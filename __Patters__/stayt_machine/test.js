function bmi(weight, height) {
    const bmi = weight / (height * height);

    const values = {
        "Underweight": bmi <= 18.5,
        "Normal": bmi <= 25.0,
        "Overweight": bmi <= 30.0,
        "Obese": true
    };
    console.log(Object.keys(values));
    return Object.keys(values).find((value) => values[value]);
}

